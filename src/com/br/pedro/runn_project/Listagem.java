    public Listagem() {
        initComponents();
        listarTodos();
        int id = IdDAO.pegarID();
        String nome = "";
        for (Login l : LoginDAO.listarTodosLogin()) {
            if (id == l.getId()) {
                nome = l.getLogin();
            }
        }
        lblNome.setText(nome);
    }

