    public void listarTodos() {
        ContatoDAO c = new ContatoDAO();
        List<Contato> l = c.listarTodosContato();

        DefaultTableModel tabela = (DefaultTableModel) tblL.getModel();

        for (Contato g : l) {
            if (IdDAO.pegarID() == g.getLogin().getId()) {
                Object[] obj = new Object[]{
                    g.getId(),
                    g.getNome_contato(),
                    g.getNumero_contato()
                };
                tabela.addRow(obj);
            }

        }
    }

