    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Login l = new Login();
        l.setLogin(txtLogin.getText().toString());
        l.setSenha(txtSenha.getText().toString());
        int i = 0;
        for (Login g : LoginDAO.listarTodosLogin()) {
            if (g.getLogin().equals(l.getLogin()) && g.getSenha().equals(l.getSenha())) {
                new TelaContato().setVisible(true);
                i++;
                IdDAO.salvarID(g.getId());
            }

        }
        if (i == 0) {
            JOptionPane.showMessageDialog(null, "usuario ou senha inv√°lidos");
        }
        System.out.println(IdDAO.pegarID());
    }//GEN-LAST:event_jButton1ActionPerformed

