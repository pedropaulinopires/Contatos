    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // pegar o  id
        try {
            Contato c = new Contato();
            Login l = new Login();
            Integer id = IdDAO.pegarID();
            l.setId(id);
            c.setLogin(l);
            c.setNome_contato(txtNome.getText().toString());
            c.setNumero_contato(txtNum.getText().toString());
            ContatoDAO.inserirContato(c);
            JOptionPane.showMessageDialog(null, "Contato salvo com sucesso!");
            txtNum.setText("");
            txtNome.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "erro ao salvar contato " + e.getMessage());
        }


    }//GEN-LAST:event_jButton1ActionPerformed

